public class LowerUpperAlphabets {
    public static void main(String[] args) {
        char ch;
        System.out.println("Lower Alphabets");
        for(ch=97;ch<=122;ch++)
        {
            System.out.print(ch+" ");
        }
        System.out.println();
        System.out.println("Uppercase Letters");
        for(ch=65;ch<=90;ch++)
        {
            System.out.print(ch+" ");
        }
    }
    
}
