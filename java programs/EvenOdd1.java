public class EvenOdd1 {

    public static void main(String[] args)
    {
        int n=5;
        int r;
        r=n%2;
        if(r==0)
        {
            System.out.println("It is a even number");
        }
        else
        {
            System.out.println("It is a Odd number");
        }
    }

}